// RAC begin

int add(int a, int b)
{
  return a + b;
}

int sub(int a, int b)
{
  return a - b;
}

int minus(int a)
{
  return -a;
}

int mult(int a, int b)
{
  return a * b;
}

int div(int a, int b)
{
  return a / b;
}

int mod(int a, int b)
{
  return a % b;
}

int parenthesis(int a)
{
  return (a);
}

// RAC end
